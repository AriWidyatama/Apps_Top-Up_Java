    private double getHargaProduk(int id_produk) throws SQLException {
        String query = "SELECT harga FROM produk WHERE id_produk = ?";
        Connection con = new Koneksi().getConnection();
        PreparedStatement pstm = con.prepareStatement(query);
        pstm.setInt(1, id_produk);

        ResultSet rs = pstm.executeQuery();
        double harga = 0;
        if (rs.next()) {
            harga = rs.getDouble("harga");
        }

        rs.close();
        pstm.close();
        con.close();

        return harga;
    }

    private void loadDataProduk(DefaultTableModel model) {
        model.setRowCount(0); // Menghapus semua baris yang ada di tabel sebelum menambahkan data baru
        Connection con = null;
        Statement stm = null;
        ResultSet rs = null;

        try {
            con = new Koneksi().getConnection();
            stm = con.createStatement();
            rs = stm.executeQuery("SELECT nama_produk, harga, stok FROM produk");

            while (rs.next()) {
                String nama = rs.getString("nama_produk");
                double harga = rs.getDouble("harga");
                int stok = rs.getInt("stok");
                model.addRow(new Object[]{nama, harga, stok});
            }
        } catch (SQLException e) {
            System.out.println("Error: " + e.getMessage());
        } finally {
            // Pastikan semua resource ditutup dengan benar
            try {
                if (rs != null) rs.close();
                if (stm != null) stm.close();
                if (con != null) con.close();
            } catch (SQLException e) {
                System.out.println("Error closing resources: " + e.getMessage());
            }
        }
    }


