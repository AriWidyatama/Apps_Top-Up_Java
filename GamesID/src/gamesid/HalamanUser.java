    private void btnSubmitTopUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitTopUpActionPerformed
        // TODO add your handling code here:
        try {
            String jumlahStr = txt_stok.getText();
            if (jumlahStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Silakan masukkan jumlah");
                return;
            }

            int jumlah = Integer.parseInt(jumlahStr);
            Item selectedItem = (Item) pilihItem.getSelectedItem();
            int id_produk = selectedItem.getIdProduk();
            String nama = selectedItem.getNamaProduk(); // Corrected method call to get product name
            double harga = getHargaProduk(id_produk);
            double total = jumlah * harga;

            double saldo = UserSession.getSaldo();

            if (saldo < total) {
                JOptionPane.showMessageDialog(this, "Saldo tidak mencukupi untuk melakukan transaksi, Ayo Isi Saldo Terlebih Dahulu");
                return;
            }

            int response = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin membeli " + nama + " dengan harga " + total + "?", "Konfirmasi Pembelian", JOptionPane.YES_NO_OPTION);
            if (response != JOptionPane.YES_OPTION) {
                return;
            }

            String query = "INSERT INTO transaksi (tgl_transaksi, id_pengguna, id_produk, jumlah, total) VALUES (NOW(), ?, ?, ?, ?)";
            Connection con = new Koneksi().getConnection();
            PreparedStatement pstm = con.prepareStatement(query);

            pstm.setInt(1, (int) UserSession.getIdPengguna()); // Assuming UserSession has a method to get user ID
            pstm.setInt(2, id_produk);
            pstm.setInt(3, jumlah);
            pstm.setDouble(4, total);

            int rowsInserted = pstm.executeUpdate();
            if (rowsInserted > 0) {
                double saldoBaru = saldo - total;
                UserSession.setSaldo(saldoBaru);
                JOptionPane.showMessageDialog(this, "Data Berhasil Masuk");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan data");
            }

            pstm.close();
            con.close();
            this.setVisible(false);
            new HalamanUser().setVisible(true);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka");
        }

        txt_stok.setText("");
    }//GEN-LAST:event_btnSubmitTopUpActionPerformed

