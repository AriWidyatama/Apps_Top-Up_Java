    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String saldo = txt_saldo.getText();
            if(saldo.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Silakan masukkan jumlah saldo");
                return;
            }

            int response = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menambahkan saldo sebesar " + saldo + "?", "Konfirmasi Pembelian", JOptionPane.YES_NO_OPTION);

            if (response == JOptionPane.YES_OPTION) {
                String sql = "UPDATE pengguna SET saldo = saldo + ? WHERE username = ?";
                Connection con = new Koneksi().getConnection();
                PreparedStatement pstm = con.prepareStatement(sql);
                pstm.setDouble(1, Double.parseDouble(saldo));
                pstm.setString(2, UserSession.getUsername()); // Mengambil username dari sesi

                int rowsUpdated = pstm.executeUpdate();
                if (rowsUpdated > 0) {
                    double saldoBaru = UserSession.getSaldo() + Double.parseDouble(saldo);
                    UserSession.setSaldo(saldoBaru);
                    JOptionPane.showMessageDialog(this, "Saldo berhasil ditambahkan");
                    
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menambahkan saldo");
                }

                pstm.close();
                con.close();
                this.setVisible(false);
                new HalamanUser().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Transaksi dibatalkan");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

